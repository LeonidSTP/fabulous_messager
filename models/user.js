const mongoose = require('mongoose');
const Schema = mongoose.Schema;

//defa
const userModel = new Schema({
    userName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports = mongoose.model('Users', userModel);
